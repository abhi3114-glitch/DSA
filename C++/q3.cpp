    int number;
    cout<<"Enter a number"<<endl;
    cin>>number;
    if (number==1){
        cout<<"january"<<endl;
    }
    else if (number==2){
        cout<<"february"<<endl;
    }
    else if (number==3){
        cout<<"march"<<endl;
    }
    else if (number==4){
        cout<<"april"<<endl;
    }
    else if (number==5){
        cout<<"may"<<endl;
    }
    else if (number==6){
        cout<<"june"<<endl;
    }
    else if (number==7){
        cout<<"july"<<endl;
    }
    else if (number==8){
        cout<<"august"<<endl;
    }
    else if (number==9){
        cout<<"september"<<endl;
    }
    else if (number==10){
        cout<<"october"<<endl;
    }
    else if (number==11){
        cout<<"november"<<endl;
    }
    else if (number==12){
        cout<<"december"<<endl;
    }
    else{
        cout<<"invalid input"<<endl;
    }
